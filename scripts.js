function createDiv(quote, author, dateCalendar) {

    var board = document.getElementById("board");
    var List_item = document.createElement("li");
    board.appendChild(List_item);
    
    first_div = document.createElement("div");
    time = document.createElement("time")
    time.innerText = dateCalendar;
    List_item.classList.add("in-view");
    List_item.appendChild(first_div);


    second_div = document.createElement("div");
    second_div.classList.add("quote");
    first_div.appendChild(time)
    first_div.appendChild(second_div)
    
    second_div_h1 = document.createElement("h1");
    second_div_h1.innerText = "Quote";
    second_div_p = document.createElement("p");
    second_div_p.innerText = quote;

    third_div = document.createElement("div");
    third_div.classList.add("Author");
    second_div.appendChild(second_div_h1);
    second_div.appendChild(second_div_p);

    third_div_h1 = document.createElement("h1");
    third_div_h1.innerText = "Author";
    third_div_span = document.createElement("span");
    third_div_span.innerText = author;

    first_div.appendChild(third_div)
    third_div.appendChild(third_div_h1);
    third_div.appendChild(third_div_span);
    return List_item;
  }
